def validate_input_ranges(values):
    for i, col in enumerate(FEATURE_COLUMNS):
        min_v, max_v = feature_ranges[col]
        if values[i] < min_v or values[i] > max_v:
            messagebox.showerror(
                "Input Error",
                f"{col} out of range!\nAllowed: {min_v} to {max_v}\nYou entered: {values[i]}"
            )
            return False
    return True

