# ----------------- Helper functions -----------------
def haversine_distance(lat1, lon1, lat2, lon2):
    R = 6371e3
    phi1 = np.radians(lat1)
    phi2 = np.radians(lat2)
    dphi = np.radians(lat2 - lat1)
    dlambda = np.radians(lon2 - lon1)
    a = np.sin(dphi / 2)**2 + np.cos(phi1) * np.cos(phi2) * np.sin(dlambda / 2)**2
    return R * 2 * np.arctan2(np.sqrt(a), np.sqrt(1 - a))

def calculate_azimuth(lat1, lon1, lat2, lon2):
    dlon = np.radians(lon2 - lon1)
    lat1_r = np.radians(lat1)
    lat2_r = np.radians(lat2)
    x = np.sin(dlon) * np.cos(lat2_r)
    y = np.cos(lat1_r) * np.sin(lat2_r) - np.sin(lat1_r) * np.cos(lat2_r) * np.cos(dlon)
    return (np.degrees(np.arctan2(x, y)) + 360) % 360

def calculate_elevation(tx_h, rx_h, distance_m):
    if distance_m == 0:
        return 0.0
    return np.degrees(np.arctan((tx_h - rx_h) / distance_m))

def normalize_expected_names(expected_iterable):
    return [str(x).replace("b'", "").replace("'", "") for x in expected_iterable]

# ----------------- Prediction -----------------
result_label = ctk.CTkLabel(app, text="", font=ctk.CTkFont(size=14, weight="bold"))
result_label.pack(pady=(6, 6))

def predict_and_optionally_send():
    try:
        values = [float(entries[i].get()) for i in range(8)]
    except Exception as e:
        messagebox.showerror("Input Error", f"Enter valid numeric values.\n{e}")
        return

    if not validate_input_ranges(values):
        return

    TxLat, TxLon, RxLat, RxLon, TxH, RxH, TxP, FreqMHz = values

    df = pd.DataFrame([{
        "TxLat": TxLat, "TxLon": TxLon,
        "RxLat": RxLat, "RxLon": RxLon,
        "TxHeight_m": TxH, "RxHeight_m": RxH,
        "TxPower_dBm": TxP, "Frequency_MHz": FreqMHz
    }])

    df["Distance_m"] = haversine_distance(TxLat, TxLon, RxLat, RxLon)
    df["Azimuth_deg"] = calculate_azimuth(TxLat, TxLon, RxLat, RxLon)
    df["Elevation_deg"] = calculate_elevation(TxH, RxH, float(df["Distance_m"].iloc[0]))
    df["Frequency_Hz"] = FreqMHz * 1e6

    eps = 1e-9
    df["Log_Distance"] = np.log(df["Distance_m"] + eps)
    df["Inv_Distance"] = 1 / (df["Distance_m"] + eps)
    df["FSPL_dB"] = (
        20 * np.log10((df["Distance_m"] / 1000) + eps)
        + 20 * np.log10(df["Frequency_MHz"] + eps)
        + 32.44
    )
    df["Height_Diff"] = df["TxHeight_m"] - df["RxHeight_m"]
    df["Height_Ratio"] = df["TxHeight_m"] / (df["RxHeight_m"] + eps)

    try:
        expected = getattr(model, "feature_names_in_", None)
        if expected is not None:
            expected_list = normalize_expected_names(expected)
            df_to_pred = df[expected_list]
        else:
            df_to_pred = df
    except Exception as e:
        messagebox.showerror("Feature Error", str(e))
        return

    # -------- SAFE PREDICTION  --------
    try:
        X = df_to_pred.to_numpy(dtype=np.float64)

        # Try normal predict first
        pred = model.predict(X)

        if isinstance(pred, np.ndarray):
            pred = float(pred[0])
        else:
            pred = float(pred)

    except Exception as e:
        try:
            # Backup LightGBM-safe method
            pred = model.booster_.predict(X)[0]
        except Exception as e2:
            messagebox.showerror(
                "Prediction Error",
                f"LightGBM EXE compatibility error.\n\n"
                f"Primary Error:\n{e}\n\n"
                f"Backup Error:\n{e2}"
            )
            return

    result_label.configure(text=f"Predicted RSSI: {pred:.2f} dBm")

# ===== BUTTON =====
ctk.CTkButton(app, text="Predict RSSI", command=predict_and_optionally_send).pack(pady=15)

